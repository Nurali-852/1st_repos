#The following code example would print the data type of x, what data type would that be?

x = 5
print(type(x)) #data type of x: <class 'int'>

x = "Hello World"
print(type(x)) #str

x = 20.5
print(type(x)) #float

x = ["apple", "banana", "cherry"]
print(type(x)) #list

x = ("apple", "banana", "cherry")
print(type(x)) #tuple

x = {"name" : "John", "age" : 36}
print(type(x)) #dict

x = True
print(type(x)) #bool